#include "libft.h"
#include <stdio.h>

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	unsigned char		*dest;
	unsigned char		*source;

	dest = (unsigned char *) dst;
	source = (unsigned char *) src;
	if (dst > src)
	{
		while (len--)
		{
			dest[len] = source[len];
		}
	}
	else
		dest = ft_memcpy(dst, src, len);
	return ((void *) dest);
}
