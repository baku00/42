#include <stdlib.h>
#include "libft.h"

char	*ft_strdup(const char *str)
{
	char	*dest;
	char	*src;
	int		i;

	src = (char *) str;
	dest = malloc((ft_strlen(src) + 1) * sizeof(char));
	if (!dest)
		return (0);
	i = 0;
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
